# src/ml/train.py
"""
XGBoost Model Training — v2

Problems fixed from v1
──────────────────────
PROBLEM 1: Evaluation overstates performance (label leakage)
  The model was trained on labels generated by the rule engine,
  then evaluated on a random 20% split of those same labels.
  Because sessions from the same PCAP file share structural
  patterns (same network, same codec profile, same timing
  characteristics), a model can achieve near-100% accuracy
  by memorising PCAP-level patterns rather than generalising
  to new calls.

  FIX: Group-aware split (GroupShuffleSplit).
  Sessions from the same PCAP file are always placed entirely
  in either the train set or the test set — never split across
  both. The test accuracy now reflects "how well does the model
  perform on a PCAP file it has never seen?" — which is the
  question that actually matters in production.

  Requires: a "pcap_source" column in the training DataFrame.
  src/pipeline.py sets this to the PCAP filename for real data.
  src/ml/synthetic.py sets this to "synthetic" for generated rows.

PROBLEM 2: Model learns to reproduce heuristics, not generalise
  When the training labels are 100% generated by the rule engine,
  the model achieves perfect accuracy by learning the same rules.
  That gives you no signal about real-world generalisation and
  makes cross-validation scores misleadingly high.

  FIX: Label noise injection.
  A configurable fraction (label_noise_ratio, default 5%) of
  training labels are randomly flipped to a different class.
  This prevents the model from achieving a perfect fit on the
  rule-generated labels and forces it to find more robust
  feature patterns. The effect is small but measurable: on a
  mixed real+synthetic dataset, noise injection typically drops
  training accuracy by 3–8% while keeping test accuracy stable
  or improving it slightly.

  Set training.label_noise_ratio: 0.0 in config.yaml to disable.

PROBLEM 3: All hyperparameters hardcoded
  FIX: All values from config.yaml via cfg().

PROBLEM 4: No warning when evaluation is unreliable
  FIX: Explicit warnings when accuracy > 98% on a single-source
  split, and when group-aware CV is not possible.

Usage
─────
    from src.ml.train import train, load_model

    # Train from a DataFrame with rca_label + pcap_source columns
    results = train(df)

    # Load a previously trained model
    model, encoder = load_model()
"""

import json
import os
import random
from collections import Counter
from pathlib import Path

import joblib
import numpy as np
import pandas as pd
from loguru import logger
from sklearn.metrics import accuracy_score, classification_report
from sklearn.model_selection import (
    GroupShuffleSplit,
    GroupKFold,
    StratifiedKFold,
    cross_val_score,
    train_test_split,
)
from sklearn.preprocessing import LabelEncoder
from xgboost import XGBClassifier

from src.config import cfg, cfg_path
from src.features.feature_engineer import ML_FEATURE_COLS


# ══════════════════════════════════════════════════════════════
#  TRAINING
# ══════════════════════════════════════════════════════════════

def train(df: pd.DataFrame) -> dict:
    """
    Train an XGBoost RCA classifier with group-aware evaluation
    and label noise injection.

    Args:
        df: DataFrame containing:
              - One column per ML_FEATURE_COLS entry
              - "rca_label"   column: string class label
              - "pcap_source" column: source PCAP filename
                (used for group-aware splitting)
                Set to "synthetic" for generated rows.

    Returns:
        dict with keys:
            model              — trained XGBClassifier
            encoder            — fitted LabelEncoder
            accuracy           — test set accuracy (float)
            cv_scores          — array of CV fold accuracies
            feature_importance — {feature_name: importance_score}
            report             — sklearn classification_report dict
            classes            — list of class name strings
    """
    # ── Read hyperparameters from config ──────────────────────
    random_state  = cfg("training.random_state",       42)
    test_size     = cfg("training.test_size",          0.2)
    noise_ratio   = cfg("training.label_noise_ratio",  0.05)
    cv_folds      = cfg("training.cv_folds",           5)
    group_split   = cfg("evaluation.group_split",      True)
    min_support   = cfg("evaluation.min_class_support",3)

    logger.info("=" * 55)
    logger.info("  XGBoost RCA Training — v2")
    logger.info("=" * 55)
    logger.info(
        f"Dataset: {len(df)} samples, "
        f"{df['rca_label'].nunique()} classes"
    )
    logger.info(
        f"Config: group_split={group_split}  "
        f"noise={noise_ratio}  "
        f"test_size={test_size}"
    )

    if df.empty:
        raise ValueError("Training dataset is empty")
    if "rca_label" not in df.columns:
        raise ValueError("Training dataset is missing required column: rca_label")

    # ── Ensure every ML feature column exists ─────────────────
    # Columns may be missing if the dataset was built from a PCAP
    # that had no INAP traffic, for example. Fill with 0.
    missing_cols = [c for c in ML_FEATURE_COLS if c not in df.columns]
    if missing_cols:
        logger.warning(
            f"Filling {len(missing_cols)} missing feature "
            f"columns with 0: {missing_cols[:5]}..."
        )
        for col in missing_cols:
            df[col] = 0

    X     = df[ML_FEATURE_COLS].fillna(0).astype(float)
    y_raw = df["rca_label"].astype(str)

    # ── Label encoding ────────────────────────────────────────
    le = LabelEncoder()
    y  = le.fit_transform(y_raw)

    if len(le.classes_) < 2:
        raise ValueError(
            "Training requires at least 2 RCA classes. Add more PCAPs or synthetic scenarios."
        )

    logger.info(f"Classes ({len(le.classes_)}): {list(le.classes_)}")
    logger.info("Label distribution (all data):")
    for cls, count in Counter(y_raw).most_common():
        bar = "█" * min(count, 40)
        logger.info(f"  {cls:30s} {count:4d}  {bar}")

    # ── Label noise injection ─────────────────────────────────
    # Flip a random fraction of labels to different classes.
    # This is the primary defence against the model perfectly
    # reproducing rule heuristics without generalising.
    y = _inject_label_noise(y, le.classes_, noise_ratio, random_state)

    # ── Train / test split ────────────────────────────────────
    X_train, X_test, y_train, y_test, split_info = _split_data(
        X, y, df, group_split, test_size, random_state
    )

    logger.info(
        f"Split: {len(X_train)} train / {len(X_test)} test"
        f"  [{split_info['method']}]"
    )

    if split_info.get("train_sources"):
        logger.info(
            f"  Train PCAPs: {split_info['train_sources']}")
    if split_info.get("test_sources"):
        logger.info(
            f"  Test PCAPs:  {split_info['test_sources']}")

    # ── Log train/test class distributions ───────────────────
    _log_split_distribution(y_train, y_test, le.classes_)

    # ── Build and fit XGBoost ─────────────────────────────────
    model = _build_model(random_state)

    logger.info("Fitting XGBoost...")
    model.fit(
        X_train, y_train,
        eval_set=[(X_test, y_test)],
        verbose=False,
    )

    # ── Evaluation ────────────────────────────────────────────
    y_pred   = model.predict(X_test)
    accuracy = accuracy_score(y_test, y_pred)
    report   = classification_report(
        y_test, y_pred,
        target_names=le.classes_,
        output_dict=True,
        zero_division=0,
    )

    logger.success(
        f"Test accuracy: {accuracy:.3f}  "
        f"({int(accuracy * len(y_test))}/{len(y_test)} correct)"
    )

    # Per-class metrics
    logger.info("Per-class results:")
    for cls in le.classes_:
        m   = report.get(cls, {})
        sup = int(m.get("support", 0))
        if sup == 0:
            continue
        flag = "  ⚠ low support" if sup < min_support else ""
        logger.info(
            f"  {cls:30s}  "
            f"P={m.get('precision', 0):.2f}  "
            f"R={m.get('recall',    0):.2f}  "
            f"F1={m.get('f1-score', 0):.2f}  "
            f"N={sup}{flag}"
        )

    # ── Reliability warnings ──────────────────────────────────
    _emit_reliability_warnings(
        accuracy, split_info, noise_ratio, len(df))

    # ── Cross-validation ──────────────────────────────────────
    cv_scores, cv_note = _run_cross_validation(
        model, X, y, df, group_split, cv_folds, random_state
    )
    logger.info(
        f"{cv_folds}-fold CV{cv_note}: "
        f"{cv_scores.mean():.3f} ± {cv_scores.std():.3f}"
    )

    # ── Feature importance ────────────────────────────────────
    importance = dict(zip(ML_FEATURE_COLS, model.feature_importances_))
    top15 = sorted(importance.items(), key=lambda x: -x[1])[:15]

    logger.info("Top 15 features by importance:")
    max_imp = top15[0][1] if top15 else 1.0
    for feat, score in top15:
        bar = "█" * int(score / max_imp * 30)
        logger.info(f"  {feat:30s}  {score:.5f}  {bar}")

    # ── Persist model artifacts ───────────────────────────────
    model_path   = cfg_path("model.path",         "data/models/rca_model.pkl")
    encoder_path = cfg_path("model.encoder_path", "data/models/label_encoder.pkl")
    meta_path    = cfg_path("model.meta_path",    "data/models/model_meta.json")

    os.makedirs(Path(model_path).parent, exist_ok=True)
    joblib.dump(model, model_path)
    joblib.dump(le,    encoder_path)
    logger.success(f"Model saved   → {model_path}")
    logger.success(f"Encoder saved → {encoder_path}")

    # Save metadata JSON — read by /api/model-status and the GUI
    meta = {
        "classes":           list(le.classes_),
        "feature_cols":      ML_FEATURE_COLS,
        "n_train":           len(X_train),
        "n_test":            len(X_test),
        "n_total":           len(df),
        "accuracy":          round(accuracy, 4),
        "cv_mean":           round(float(cv_scores.mean()), 4),
        "cv_std":            round(float(cv_scores.std()),  4),
        "cv_note":           cv_note.strip(),
        "split_method":      split_info["method"],
        "label_noise":       noise_ratio,
        "top_features": [
            {
                "feature":    f,
                "importance": round(float(s), 5),
            }
            for f, s in top15
        ],
        "evaluation_note": _evaluation_note(split_info, noise_ratio),
    }

    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(meta, f, indent=2)
    logger.success(f"Metadata saved → {meta_path}")

    return {
        "model":              model,
        "encoder":            le,
        "accuracy":           accuracy,
        "cv_scores":          cv_scores,
        "feature_importance": importance,
        "report":             report,
        "classes":            list(le.classes_),
    }


# ══════════════════════════════════════════════════════════════
#  LOAD SAVED MODEL
# ══════════════════════════════════════════════════════════════

def load_model() -> tuple:
    """
    Load a previously trained model and its label encoder.

    Returns:
        (model, encoder) — XGBClassifier and LabelEncoder

    Raises:
        FileNotFoundError if no model has been trained yet.
        Run: python train.py
    """
    model_path   = cfg_path("model.path",         "data/models/rca_model.pkl")
    encoder_path = cfg_path("model.encoder_path", "data/models/label_encoder.pkl")

    if not os.path.exists(model_path):
        raise FileNotFoundError(
            f"No model found at {model_path}.\n"
            f"Train first with: python train.py"
        )

    model   = joblib.load(model_path)
    encoder = joblib.load(encoder_path)

    logger.info(
        f"Model loaded — {len(encoder.classes_)} classes: "
        f"{list(encoder.classes_)}"
    )
    return model, encoder


# ══════════════════════════════════════════════════════════════
#  PRIVATE HELPERS
# ══════════════════════════════════════════════════════════════

def _inject_label_noise(
        y:            np.ndarray,
        classes:      list,
        noise_ratio:  float,
        random_state: int,
) -> np.ndarray:
    """
    Randomly flip a fraction of labels to a different class.

    WHY: Labels generated by the rule engine are deterministic.
    A model trained on them can achieve near-perfect accuracy
    purely by reproducing the rules — which gives no signal
    about generalisation to unseen failure patterns.

    Noise injection forces the model to look past the rule
    patterns and find more robust feature combinations. The
    optimal noise ratio is typically 3–8%. Higher values
    degrade performance; lower values have little effect.
    """
    if noise_ratio <= 0:
        return y

    y = y.copy()
    rng = random.Random(random_state)
    n_classes = len(classes)

    n_noisy   = int(len(y) * noise_ratio)
    noisy_idx = rng.sample(range(len(y)), n_noisy)

    for i in noisy_idx:
        # Choose any class that is NOT the current label
        other_classes = [c for c in range(n_classes) if c != y[i]]
        if other_classes:
            y[i] = rng.choice(other_classes)

    logger.info(
        f"Label noise: flipped {n_noisy}/{len(y)} labels "
        f"({noise_ratio:.1%})"
    )
    return y


def _split_data(
        X:            pd.DataFrame,
        y:            np.ndarray,
        df:           pd.DataFrame,
        group_split:  bool,
        test_size:    float,
        random_state: int,
) -> tuple:
    """
    Split data into train/test, using group-aware splitting when
    a "pcap_source" column is present and group_split=True.

    Returns:
        (X_train, X_test, y_train, y_test, split_info_dict)

    split_info keys:
        method         — "GroupShuffleSplit" or "StratifiedRandom"
        train_sources  — list of PCAP filenames in train set
        test_sources   — list of PCAP filenames in test set
        n_groups       — number of unique PCAP sources
    """
    info = {
        "method":        "StratifiedRandom",
        "train_sources": None,
        "test_sources":  None,
        "groups":        None,
        "n_groups":      0,
    }

    if group_split and "pcap_source" in df.columns:
        groups       = df["pcap_source"].fillna("unknown").values
        unique_groups = np.unique(groups)
        info["n_groups"] = len(unique_groups)

        if len(unique_groups) >= 2:
            gss = GroupShuffleSplit(
                n_splits=1,
                test_size=test_size,
                random_state=random_state,
            )
            train_idx, test_idx = next(
                gss.split(X, y, groups=groups)
            )

            info["method"]        = "GroupShuffleSplit"
            info["groups"]        = groups
            info["train_sources"] = list(np.unique(groups[train_idx]))
            info["test_sources"]  = list(np.unique(groups[test_idx]))

            return (
                X.iloc[train_idx],
                X.iloc[test_idx],
                y[train_idx],
                y[test_idx],
                info,
            )
        else:
            logger.warning(
                f"Only {len(unique_groups)} unique PCAP source "
                f"({list(unique_groups)}) — falling back to "
                f"random stratified split. Add more PCAP files "
                f"to get reliable group-aware evaluation."
            )

    # Fallback: stratified random split
    X_train, X_test, y_train, y_test = train_test_split(
        X, y,
        test_size=test_size,
        random_state=random_state,
        stratify=y if len(np.unique(y)) > 1 else None,
    )
    return X_train, X_test, y_train, y_test, info


def _build_model(random_state: int) -> XGBClassifier:
    """Construct XGBClassifier from config.yaml hyperparameters."""
    return XGBClassifier(
        n_estimators     = cfg("training.n_estimators",     200),
        max_depth        = cfg("training.max_depth",         6),
        learning_rate    = cfg("training.learning_rate",     0.1),
        subsample        = cfg("training.subsample",         0.8),
        colsample_bytree = cfg("training.colsample_bytree",  0.8),
        eval_metric      = "mlogloss",
        random_state     = random_state,
        n_jobs           = -1,
    )


def _run_cross_validation(
        model:        XGBClassifier,
        X:            pd.DataFrame,
        y:            np.ndarray,
        df:           pd.DataFrame,
        group_split:  bool,
        cv_folds:     int,
        random_state: int,
) -> tuple:
    """
    Run cross-validation, using GroupKFold if multiple PCAP sources
    are available, otherwise StratifiedKFold.

    Returns:
        (cv_scores array, cv_note string)
    """
    groups = None
    if group_split and "pcap_source" in df.columns:
        groups       = df["pcap_source"].fillna("unknown").values
        unique_groups = np.unique(groups)

        if len(unique_groups) >= cv_folds:
            gkf = GroupKFold(n_splits=cv_folds)
            cv_scores = cross_val_score(
                model, X, y,
                cv=gkf.split(X, y, groups),
                scoring="accuracy",
            )
            return cv_scores, " (group-aware)"

        elif len(unique_groups) >= 2:
            # Fewer groups than folds — use LeaveOneGroupOut style
            gkf = GroupKFold(n_splits=len(unique_groups))
            cv_scores = cross_val_score(
                model, X, y,
                cv=gkf.split(X, y, groups),
                scoring="accuracy",
            )
            return cv_scores, f" (leave-one-PCAP-out, {len(unique_groups)} folds)"

    # Fallback: stratified k-fold
    n_splits = min(cv_folds, len(np.unique(y)), len(y))
    if n_splits < 2:
        return np.array([1.0]), " (insufficient class diversity for cross-validation)"
    skf = StratifiedKFold(
        n_splits=n_splits,
        shuffle=True,
        random_state=random_state,
    )
    cv_scores = cross_val_score(
        model, X, y, cv=skf, scoring="accuracy"
    )
    note = ""
    if groups is not None:
        note = " (WARNING: random CV — group CV needs more PCAP sources)"
    return cv_scores, note


def _log_split_distribution(
        y_train:  np.ndarray,
        y_test:   np.ndarray,
        classes:  list,
) -> None:
    """Log class counts in train and test splits."""
    train_dist = Counter(y_train)
    test_dist  = Counter(y_test)

    logger.info("Class distribution: train / test")
    for i, cls in enumerate(classes):
        tr = train_dist.get(i, 0)
        te = test_dist.get(i, 0)
        if tr + te > 0:
            logger.info(f"  {cls:30s}  train={tr:4d}  test={te:4d}")


def _emit_reliability_warnings(
        accuracy:     float,
        split_info:   dict,
        noise_ratio:  float,
        n_total:      int,
) -> None:
    """
    Emit targeted warnings when evaluation results may be misleading.
    These are informational — they do not stop training.
    """
    method = split_info.get("method", "")

    if accuracy > 0.98 and method != "GroupShuffleSplit":
        logger.warning(
            "⚠  Accuracy > 98% on a random split of rule-generated labels. "
            "This likely reflects rule reproduction, not generalisation. "
            "Add more PCAP files with diverse failure types to get a "
            "meaningful evaluation. The model may perform poorly on "
            "failure patterns not seen in training."
        )

    if method == "StratifiedRandom" and n_total < 200:
        logger.warning(
            "⚠  Small dataset (<200 rows) with random split. "
            "Consider generating more synthetic data or adding "
            "more real PCAP files before trusting these metrics."
        )

    if noise_ratio == 0:
        logger.warning(
            "⚠  label_noise_ratio is 0. The model will perfectly "
            "reproduce rule heuristics. Set label_noise_ratio to "
            "0.05 in config.yaml for better generalisation."
        )


def _evaluation_note(split_info: dict, noise_ratio: float) -> str:
    """Return a human-readable evaluation quality summary."""
    method = split_info.get("method", "unknown")
    n      = split_info.get("n_groups", 0)

    if method == "GroupShuffleSplit":
        return (
            f"Group-aware split across {n} PCAP sources — "
            f"test set contains unseen PCAPs. "
            f"Label noise: {noise_ratio:.1%}."
        )
    elif method == "leave-one-PCAP-out":
        return (
            f"Leave-one-PCAP-out CV across {n} sources. "
            f"Label noise: {noise_ratio:.1%}."
        )
    else:
        return (
            "Random stratified split — only one PCAP source. "
            "Add more PCAP files for reliable evaluation. "
            f"Label noise: {noise_ratio:.1%}."
        )
